public class Main {
    public static void main(String[] args) {
        // Integer type
        int age = 22;
        
        // Floating point type
        float pi = 3.14f;
        
        // Double type
        double largePi = 3.1415926535;
        
        // Character type
        char grade = 'A';
        
        // Boolean type
        boolean isJavaFun = true;

        // Output all values
        System.out.println("Age: " + age);
        System.out.println("Float PI: " + pi);
        System.out.println("Double PI: " + largePi);
        System.out.println("Grade: " + grade);
        System.out.println("Is Java Fun? " + isJavaFun);
    }
}
